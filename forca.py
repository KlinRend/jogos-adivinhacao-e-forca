def define_palavra():
    # leitura do arquivo com as frutas; limpando a informação com .strip e adicionando em uma lista com .append
    palavras = []
    with open("palavras.txt") as arquivo:  # escrever assim para garantir fechamento do arquivo em caso de erro
        for linha in arquivo:
            linha = linha.strip()
            palavras.append(linha)

    # selecionando aleatoriamente um elemento a lista criado com a bib RANDOM
    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta


def inicializa_letras_acertadas(palavra):
    return ["_" for letra in palavra]


def chute_jogador():
    chute = input("Qual letra? ")
    chute = chute.strip().upper()
    return chute


def chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute.upper() == letra.upper()):
            letras_acertadas[index] = letra
        index += 1


